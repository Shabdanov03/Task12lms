public class Car {
    private String brend;
    private int speed;
    private int price;

    public void setBrend(String brend){
        this.brend=brend;
    }
    public void setSpeed(int speed){
        this.speed=speed;
    }
    public void setPrice(int price){
        this.price=price;
    }

    public String getBrend(){
        return brend;
    }
    public int getSpeed(){
        return speed;
    }
    public int getPrice(){
        return price;
    }
}
