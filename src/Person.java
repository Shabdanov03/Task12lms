public class Person {
    private String fullName;
    private int age;
    private int id;

    public void setFullName(String fullName){
     this.fullName=fullName;
    }
    public void setAge(int age){
        this.age=age;
    }
    public void setId(int id){
        this.id=id;
    }

    public String getFullName(){
        return fullName;
    }
    public int getAge(){
        return age;
    }
    public int getId() {
        return id;
    }
}
