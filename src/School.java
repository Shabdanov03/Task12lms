public class School {
    private String adres;
    private String name;
    private int klass;

    public void setAdres(String adres){
        this.adres=adres;
    }
    public void setName(String name){
        this.name=name;
    }
    public void  setKlass(int klass){
        this.klass=klass;
    }

    public String getAdres(){
        return adres;
    }
    public String getName(){
        return name;
    }
    public int getKlass(){
        return klass;
    }
}
